package com.infinity.infoway.rkuniversity.rku_old_app.models;

public class PGScholarGuidedModel {

    String srNo;
    String pgScholarName;
    String otherGuideName;
    String titleOfResearch;
    String registretionYear;
    String awardedYear;
    String pgScholarStatus;

    public String getSrNo() {
        return srNo;
    }

    public void setSrNo(String srNo) {
        this.srNo = srNo;
    }

    public String getPgScholarName() {
        return pgScholarName;
    }

    public void setPgScholarName(String pgScholarName) {
        this.pgScholarName = pgScholarName;
    }

    public String getOtherGuideName() {
        return otherGuideName;
    }

    public void setOtherGuideName(String otherGuideName) {
        this.otherGuideName = otherGuideName;
    }

    public String getTitleOfResearch() {
        return titleOfResearch;
    }

    public void setTitleOfResearch(String titleOfResearch) {
        this.titleOfResearch = titleOfResearch;
    }

    public String getRegistretionYear() {
        return registretionYear;
    }

    public void setRegistretionYear(String registretionYear) {
        this.registretionYear = registretionYear;
    }

    public String getAwardedYear() {
        return awardedYear;
    }

    public void setAwardedYear(String awardedYear) {
        this.awardedYear = awardedYear;
    }

    public String getPgScholarStatus() {
        return pgScholarStatus;
    }

    public void setPgScholarStatus(String pgScholarStatus) {
        this.pgScholarStatus = pgScholarStatus;
    }
}
