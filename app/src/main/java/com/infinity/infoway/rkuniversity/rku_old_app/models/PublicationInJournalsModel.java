package com.infinity.infoway.rkuniversity.rku_old_app.models;

public class PublicationInJournalsModel {


    String srNo;
    String researchPaper;
    String authorType;
    String level;
    String journal;
    String nosOfAuthor;
    String yearOfPublication;
    String proceedingsISBN;
    String categoryInUGC;

    public String getSrNo() {
        return srNo;
    }

    public void setSrNo(String srNo) {
        this.srNo = srNo;
    }

    public String getResearchPaper() {
        return researchPaper;
    }

    public void setResearchPaper(String researchPaper) {
        this.researchPaper = researchPaper;
    }

    public String getAuthorType() {
        return authorType;
    }

    public void setAuthorType(String authorType) {
        this.authorType = authorType;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getJournal() {
        return journal;
    }

    public void setJournal(String journal) {
        this.journal = journal;
    }

    public String getNosOfAuthor() {
        return nosOfAuthor;
    }

    public void setNosOfAuthor(String nosOfAuthor) {
        this.nosOfAuthor = nosOfAuthor;
    }

    public String getYearOfPublication() {
        return yearOfPublication;
    }

    public void setYearOfPublication(String yearOfPublication) {
        this.yearOfPublication = yearOfPublication;
    }

    public String getProceedingsISBN() {
        return proceedingsISBN;
    }

    public void setProceedingsISBN(String proceedingsISBN) {
        this.proceedingsISBN = proceedingsISBN;
    }

    public String getCategoryInUGC() {
        return categoryInUGC;
    }

    public void setCategoryInUGC(String categoryInUGC) {
        this.categoryInUGC = categoryInUGC;
    }
}
