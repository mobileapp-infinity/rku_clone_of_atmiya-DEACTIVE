package com.infinity.infoway.rkuniversity.rku_old_app.models;

public class PDApprovalRemarksAndReasonListModel {


    String id;
    String employeeName;
    String reason;
    String pdFrameworkCredit;
    String noOfOnDutyLeaves;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getPdFrameworkCredit() {
        return pdFrameworkCredit;
    }

    public void setPdFrameworkCredit(String pdFrameworkCredit) {
        this.pdFrameworkCredit = pdFrameworkCredit;
    }

    public String getNoOfOnDutyLeaves() {
        return noOfOnDutyLeaves;
    }

    public void setNoOfOnDutyLeaves(String noOfOnDutyLeaves) {
        this.noOfOnDutyLeaves = noOfOnDutyLeaves;
    }
}
